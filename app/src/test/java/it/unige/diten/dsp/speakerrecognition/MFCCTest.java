    public boolean areEqual(double a, double b)
    {
        double abs = a-b < 0 ? (b-a) : (a-b);
        return(abs <= 0.1);
    }

    private double[][] MFCCExtractor(String filename) throws Exception
    {
        Framer.readFromFile(filename);
        Frame[] frames = Framer.getFrames();

        double[][] MFCC = new double[frames.length][];
        for (int frame = 0; frame < frames.length; frame++)
        {
            MFCC[frame] = DCT.computeDCT(
                    Logarithmer.computeLogarithm(
                            MelScaler.extractMelEnergies(
                                    Periodogrammer.computePeriodogram(
                                            frames[frame]
                                    )
                            )
                    ), FeatureExtractor.MFCC_COUNT // Only keep the first MFCC_COUNT coefficients of the resulting DCT sequence
            );
        }

        return(MFCC);

    }
